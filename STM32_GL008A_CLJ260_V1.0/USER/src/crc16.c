//	基于 Modbus 协议的 CRC16 算法
//	程序调用之后,CRCHi和CRCLo分别为计算的CRC16的高位字节和低位字节
//	主函数中调用,无返回值
void Cal_CRC(unsigned char *Data, unsigned char Len)
{
	unsigned char CRCHi = 0xFF ; 
	unsigned char CRCLo = 0xFF ;
    unsigned char Index; 
    while (Len--) 
    { 
		Index = CRCHi ^ *Data++; 
		CRCHi = CRCLo ^ auchCRCHi[Index]; 
		CRCLo = auchCRCLo[Index]; 
    }	
	CRCHigh = CRCHi;
	CRCLow  = CRCLo;
}

//modbus浮点数解析计算公式
float get_floatmodbus(u8 *p)
{
	float a;
	u8 *r;
	r=(u8 *)&a;
	*r=p[1];r++;
	*r=p[0];r++;
	*r=p[3];r++;
	*r=p[2];
	return a;
}
/*
*function：ftoc(float fvalue,unsigned char*arr)
*decription:  浮点数转化成四个字节
*input: 浮点数 
*output: 4个字节的字节数组
*/
//例如12.5--0x41 48 00 00;转换完之后，arr[0]-00，arr[1]-00，arr[2]-48，arr[3]-41
void ftoc(float fvalue,u8 *arr) 
{
    u8  *pf;
    u8 *px;
    u8 i;   //计数器 
    pf =(u8 *)&fvalue;            /*unsigned char型指针取得浮点数的首地址*/  
    px = arr;                               /*字符数组arr准备存储浮点数的四个字节,px指针指向字节数组arr*/

    for(i=0;i<4;i++)
    {
        *(px+i)=*(pf+i);     /*使用unsigned char型指针从低地址一个字节一个字节取出*/
    }
}

//  三次曲线拟合算法
void cubicSmooth5 ( float in[], float out[], u8 N )
{
 
    int i;
    if ( N < 5 )
    {
        for ( i = 0; i <= N - 1; i++ )
            out[i] = in[i];
    }
 
    else
    {
        out[0] = (69.0 * in[0] + 4.0 * in[1] - 6.0 * in[2] + 4.0 * in[3] - in[4]) / 70.0;
        out[1] = (2.0 * in[0] + 27.0 * in[1] + 12.0 * in[2] - 8.0 * in[3] + 2.0 * in[4]) / 35.0;
        for ( i = 2; i <= N - 3; i++ )
        {
            out[i] = (-3.0 * (in[i - 2] + in[i + 2])+ 12.0 * (in[i - 1] + in[i + 1]) + 17.0 * in[i] ) / 35.0;
        }
        out[N - 2] = (2.0 * in[N - 5] - 8.0 * in[N - 4] + 12.0 * in[N - 3] + 27.0 * in[N - 2] + 2.0 * in[N - 1]) / 35.0;
        out[N - 1] = (- in[N - 5] + 4.0 * in[N - 4] - 6.0 * in[N - 3] + 4.0 * in[N - 2] + 69.0 * in[N - 1]) / 70.0;
    }
    return;
}
